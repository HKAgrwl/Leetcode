class Solution{
    public:
    // arr: input array
    // n: size of array
    //Function to find the sum of contiguous subarray with maximum sum.
    long long maxSubarraySum(int arr[], int n){
        
        // Your code here
        long long mth=0;// max till here
        long long mx=-1e9-7;
        for(int i=0;i<n;i++){
            mth=mth+arr[i];
            mx=max(mx,mth);
            if(mth<0)mth=0;
        }
        return mx;
        
    }
};

